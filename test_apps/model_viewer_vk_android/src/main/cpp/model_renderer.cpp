/*!
 * @file
 * @brief  glTF 2.0 PBR model renderer — raster implementation.
 *
 * Renders the loaded model into an internal colour image with a
 * metallic-roughness PBR pass, then blits that image into the per-eye
 * swapchain viewport region. The viewport-copy + transparency dance mirrors
 * 3dgs_common/gs_renderer.cpp so this drops into the platform code unchanged.
 *
 * v1: material factors only (no textures/IBL), one directional light + flat
 * ambient, static geometry. See ../PORTING.md for the follow-ups.
 */

