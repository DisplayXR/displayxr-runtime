bool ModelRenderer::init(VkInstance instance,
                         VkPhysicalDevice physicalDevice,
                         VkDevice device,
                         VkQueue queue,
                         uint32_t queueFamilyIndex,
                         uint32_t renderWidth,
                         uint32_t renderHeight) {
    instance_ = instance;
    physDevice_ = physicalDevice;
    device_ = device;
    queue_ = queue;
    queueFamily_ = queueFamilyIndex;
    width_ = renderWidth;
    height_ = renderHeight;

    VkCommandPoolCreateInfo poolInfo = {VK_STRUCTURE_TYPE_COMMAND_POOL_CREATE_INFO};
    poolInfo.flags = VK_COMMAND_POOL_CREATE_RESET_COMMAND_BUFFER_BIT;
    poolInfo.queueFamilyIndex = queueFamilyIndex;
    if (vkCreateCommandPool(device, &poolInfo, nullptr, &cmdPool_) != VK_SUCCESS) {
        std::fprintf(stderr, "ModelRenderer: failed to create command pool\n");
        return false;
    }

    if (!createRenderTargets()) return false;
    if (!createPipeline()) return false;
    if (!createSamplerAndDefaults()) return false;
    if (!createIbl()) return false;

    initialized_ = true;
    std::printf("ModelRenderer: initialized (%ux%u)\n", width_, height_);
    return true;
}

bool ModelRenderer::createRenderTargets() {
    // Render pass (format-only; size-independent). Clears colour+depth, leaves
    // colour in TRANSFER_SRC for the per-eye viewport blit.
    VkAttachmentDescription atts[2] = {};
    atts[0].format = colorFormat_;
    atts[0].samples = VK_SAMPLE_COUNT_1_BIT;
    atts[0].loadOp = VK_ATTACHMENT_LOAD_OP_CLEAR;
    atts[0].storeOp = VK_ATTACHMENT_STORE_OP_STORE;
    atts[0].stencilLoadOp = VK_ATTACHMENT_LOAD_OP_DONT_CARE;
    atts[0].stencilStoreOp = VK_ATTACHMENT_STORE_OP_DONT_CARE;
    atts[0].initialLayout = VK_IMAGE_LAYOUT_UNDEFINED;
    atts[0].finalLayout = VK_IMAGE_LAYOUT_TRANSFER_SRC_OPTIMAL;

    atts[1].format = depthFormat_;
    atts[1].samples = VK_SAMPLE_COUNT_1_BIT;
    atts[1].loadOp = VK_ATTACHMENT_LOAD_OP_CLEAR;
    atts[1].storeOp = VK_ATTACHMENT_STORE_OP_DONT_CARE;
    atts[1].stencilLoadOp = VK_ATTACHMENT_LOAD_OP_DONT_CARE;
    atts[1].stencilStoreOp = VK_ATTACHMENT_STORE_OP_DONT_CARE;
    atts[1].initialLayout = VK_IMAGE_LAYOUT_UNDEFINED;
    atts[1].finalLayout = VK_IMAGE_LAYOUT_DEPTH_STENCIL_ATTACHMENT_OPTIMAL;

    VkAttachmentReference colorRef = {0, VK_IMAGE_LAYOUT_COLOR_ATTACHMENT_OPTIMAL};
    VkAttachmentReference depthRef = {1, VK_IMAGE_LAYOUT_DEPTH_STENCIL_ATTACHMENT_OPTIMAL};
    VkSubpassDescription sub = {};
    sub.pipelineBindPoint = VK_PIPELINE_BIND_POINT_GRAPHICS;
    sub.colorAttachmentCount = 1;
    sub.pColorAttachments = &colorRef;
    sub.pDepthStencilAttachment = &depthRef;

    VkSubpassDependency deps[2] = {};
    deps[0].srcSubpass = VK_SUBPASS_EXTERNAL;
    deps[0].dstSubpass = 0;
    deps[0].srcStageMask = VK_PIPELINE_STAGE_COLOR_ATTACHMENT_OUTPUT_BIT | VK_PIPELINE_STAGE_LATE_FRAGMENT_TESTS_BIT;
    deps[0].dstStageMask = VK_PIPELINE_STAGE_COLOR_ATTACHMENT_OUTPUT_BIT | VK_PIPELINE_STAGE_EARLY_FRAGMENT_TESTS_BIT;
    deps[0].srcAccessMask = 0;
    deps[0].dstAccessMask = VK_ACCESS_COLOR_ATTACHMENT_WRITE_BIT | VK_ACCESS_DEPTH_STENCIL_ATTACHMENT_WRITE_BIT;
    deps[1].srcSubpass = 0;
    deps[1].dstSubpass = VK_SUBPASS_EXTERNAL;
    deps[1].srcStageMask = VK_PIPELINE_STAGE_COLOR_ATTACHMENT_OUTPUT_BIT;
    deps[1].dstStageMask = VK_PIPELINE_STAGE_TRANSFER_BIT;
    deps[1].srcAccessMask = VK_ACCESS_COLOR_ATTACHMENT_WRITE_BIT;
    deps[1].dstAccessMask = VK_ACCESS_TRANSFER_READ_BIT;

    VkRenderPassCreateInfo rpci = {VK_STRUCTURE_TYPE_RENDER_PASS_CREATE_INFO};
    rpci.attachmentCount = 2;
    rpci.pAttachments = atts;
    rpci.subpassCount = 1;
    rpci.pSubpasses = &sub;
    rpci.dependencyCount = 2;
    rpci.pDependencies = deps;
    if (vkCreateRenderPass(device_, &rpci, nullptr, &renderPass_) != VK_SUCCESS) return false;

    return ensureTargets(width_, height_);
}

// (Re)create the internal colour+depth targets and framebuffer at w×h, reusing
// renderPass_. Called at init and whenever the per-eye viewport size changes
// (window resize) — a fixed-size framebuffer would make the render pass invalid
// once the window grew, leaving the swapchain unwritten (window goes transparent).
bool ModelRenderer::ensureTargets(uint32_t w, uint32_t h) {
    if (w == 0) w = 1;
    if (h == 0) h = 1;
    if (device_ != VK_NULL_HANDLE) vkDeviceWaitIdle(device_);
    if (framebuffer_ != VK_NULL_HANDLE) { vkDestroyFramebuffer(device_, framebuffer_, nullptr); framebuffer_ = VK_NULL_HANDLE; }
    if (colorImage_.image != VK_NULL_HANDLE) modelDestroyImage(device_, colorImage_);
    if (depthImage_.image != VK_NULL_HANDLE) modelDestroyImage(device_, depthImage_);
    width_ = w; height_ = h;

    colorImage_ = modelCreateImage2D(device_, physDevice_, w, h, colorFormat_,
        VK_IMAGE_USAGE_COLOR_ATTACHMENT_BIT | VK_IMAGE_USAGE_TRANSFER_SRC_BIT);
    if (colorImage_.image == VK_NULL_HANDLE) return false;

    VkImageCreateInfo ici = {VK_STRUCTURE_TYPE_IMAGE_CREATE_INFO};
    ici.imageType = VK_IMAGE_TYPE_2D; ici.format = depthFormat_;
    ici.extent = {w, h, 1}; ici.mipLevels = 1; ici.arrayLayers = 1;
    ici.samples = VK_SAMPLE_COUNT_1_BIT; ici.tiling = VK_IMAGE_TILING_OPTIMAL;
    ici.usage = VK_IMAGE_USAGE_DEPTH_STENCIL_ATTACHMENT_BIT; ici.initialLayout = VK_IMAGE_LAYOUT_UNDEFINED;
    if (vkCreateImage(device_, &ici, nullptr, &depthImage_.image) != VK_SUCCESS) return false;
    VkMemoryRequirements mr; vkGetImageMemoryRequirements(device_, depthImage_.image, &mr);
    VkMemoryAllocateInfo ai = {VK_STRUCTURE_TYPE_MEMORY_ALLOCATE_INFO};
    ai.allocationSize = mr.size;
    ai.memoryTypeIndex = modelFindMemoryType(physDevice_, mr.memoryTypeBits, VK_MEMORY_PROPERTY_DEVICE_LOCAL_BIT);
    if (vkAllocateMemory(device_, &ai, nullptr, &depthImage_.memory) != VK_SUCCESS) return false;
    vkBindImageMemory(device_, depthImage_.image, depthImage_.memory, 0);
    VkImageViewCreateInfo vci = {VK_STRUCTURE_TYPE_IMAGE_VIEW_CREATE_INFO};
    vci.image = depthImage_.image; vci.viewType = VK_IMAGE_VIEW_TYPE_2D; vci.format = depthFormat_;
    vci.subresourceRange = {VK_IMAGE_ASPECT_DEPTH_BIT, 0, 1, 0, 1};
    if (vkCreateImageView(device_, &vci, nullptr, &depthImage_.view) != VK_SUCCESS) return false;
    depthImage_.width = w; depthImage_.height = h;

    VkImageView fbViews[2] = {colorImage_.view, depthImage_.view};
    VkFramebufferCreateInfo fbci = {VK_STRUCTURE_TYPE_FRAMEBUFFER_CREATE_INFO};
    fbci.renderPass = renderPass_; fbci.attachmentCount = 2; fbci.pAttachments = fbViews;
    fbci.width = w; fbci.height = h; fbci.layers = 1;
    if (vkCreateFramebuffer(device_, &fbci, nullptr, &framebuffer_) != VK_SUCCESS) return false;
    return true;
}

bool ModelRenderer::createPipeline() {
    // Set-0: one UBO, visible to vertex + fragment.
    VkDescriptorSetLayoutBinding b0 = {};
    b0.binding = 0;
    b0.descriptorType = VK_DESCRIPTOR_TYPE_UNIFORM_BUFFER;
    b0.descriptorCount = 1;
    b0.stageFlags = VK_SHADER_STAGE_VERTEX_BIT | VK_SHADER_STAGE_FRAGMENT_BIT;
    VkDescriptorSetLayoutCreateInfo dlci = {VK_STRUCTURE_TYPE_DESCRIPTOR_SET_LAYOUT_CREATE_INFO};
    dlci.bindingCount = 1;
    dlci.pBindings = &b0;
    if (vkCreateDescriptorSetLayout(device_, &dlci, nullptr, &dsLayout_) != VK_SUCCESS) return false;

    // Set 1: per-material textures — base color, MR, normal, occlusion, emissive.
    VkDescriptorSetLayoutBinding mb[5] = {};
    for (uint32_t i = 0; i < 5; ++i) {
        mb[i].binding = i;
        mb[i].descriptorType = VK_DESCRIPTOR_TYPE_COMBINED_IMAGE_SAMPLER;
        mb[i].descriptorCount = 1;
        mb[i].stageFlags = VK_SHADER_STAGE_FRAGMENT_BIT;
    }
    VkDescriptorSetLayoutCreateInfo mlci = {VK_STRUCTURE_TYPE_DESCRIPTOR_SET_LAYOUT_CREATE_INFO};
    mlci.bindingCount = 5;
    mlci.pBindings = mb;
    if (vkCreateDescriptorSetLayout(device_, &mlci, nullptr, &matSetLayout_) != VK_SUCCESS) return false;

    // Set 2: IBL — irradiance cube, prefiltered cube, BRDF LUT (all fragment).
    VkDescriptorSetLayoutBinding ib[3] = {};
    for (uint32_t i = 0; i < 3; ++i) {
        ib[i].binding = i;
        ib[i].descriptorType = VK_DESCRIPTOR_TYPE_COMBINED_IMAGE_SAMPLER;
        ib[i].descriptorCount = 1;
        ib[i].stageFlags = VK_SHADER_STAGE_FRAGMENT_BIT;
    }
    VkDescriptorSetLayoutCreateInfo ilci = {VK_STRUCTURE_TYPE_DESCRIPTOR_SET_LAYOUT_CREATE_INFO};
    ilci.bindingCount = 3;
    ilci.pBindings = ib;
    if (vkCreateDescriptorSetLayout(device_, &ilci, nullptr, &iblSetLayout_) != VK_SUCCESS) return false;

    // Set 3: joint-matrix SSBO for skinning (vertex stage). Always present (a
    // 1-matrix dummy for static models) so the binding is valid every draw.
    VkDescriptorSetLayoutBinding jb = {};
    jb.binding = 0;
    jb.descriptorType = VK_DESCRIPTOR_TYPE_STORAGE_BUFFER;
    jb.descriptorCount = 1;
    jb.stageFlags = VK_SHADER_STAGE_VERTEX_BIT;
    VkDescriptorSetLayoutCreateInfo jlci = {VK_STRUCTURE_TYPE_DESCRIPTOR_SET_LAYOUT_CREATE_INFO};
    jlci.bindingCount = 1;
    jlci.pBindings = &jb;
    if (vkCreateDescriptorSetLayout(device_, &jlci, nullptr, &jointSetLayout_) != VK_SUCCESS) return false;

    VkPushConstantRange pcr = {};
    pcr.stageFlags = VK_SHADER_STAGE_VERTEX_BIT | VK_SHADER_STAGE_FRAGMENT_BIT;
    pcr.offset = 0;
    pcr.size = sizeof(PushBlock);
    VkDescriptorSetLayout setLayouts[4] = {dsLayout_, matSetLayout_, iblSetLayout_, jointSetLayout_};
    VkPipelineLayoutCreateInfo plci = {VK_STRUCTURE_TYPE_PIPELINE_LAYOUT_CREATE_INFO};
    plci.setLayoutCount = 4;
    plci.pSetLayouts = setLayouts;
    plci.pushConstantRangeCount = 1;
    plci.pPushConstantRanges = &pcr;
    if (vkCreatePipelineLayout(device_, &plci, nullptr, &pipelineLayout_) != VK_SUCCESS) return false;

    VkShaderModule vs = createShaderModule(device_, pbr_vert_data, sizeof(pbr_vert_data));
    VkShaderModule fs = createShaderModule(device_, pbr_frag_data, sizeof(pbr_frag_data));
    if (vs == VK_NULL_HANDLE || fs == VK_NULL_HANDLE) return false;

    VkPipelineShaderStageCreateInfo stages[2] = {};
    stages[0].sType = VK_STRUCTURE_TYPE_PIPELINE_SHADER_STAGE_CREATE_INFO;
    stages[0].stage = VK_SHADER_STAGE_VERTEX_BIT;
    stages[0].module = vs;
    stages[0].pName = "main";
    stages[1].sType = VK_STRUCTURE_TYPE_PIPELINE_SHADER_STAGE_CREATE_INFO;
    stages[1].stage = VK_SHADER_STAGE_FRAGMENT_BIT;
    stages[1].module = fs;
    stages[1].pName = "main";

    VkVertexInputBindingDescription vib = {0, sizeof(ModelVertex), VK_VERTEX_INPUT_RATE_VERTEX};
    VkVertexInputAttributeDescription via[5] = {
        {0, 0, VK_FORMAT_R32G32B32_SFLOAT,    (uint32_t)offsetof(ModelVertex, pos)},
        {1, 0, VK_FORMAT_R32G32B32_SFLOAT,    (uint32_t)offsetof(ModelVertex, normal)},
        {2, 0, VK_FORMAT_R32G32_SFLOAT,       (uint32_t)offsetof(ModelVertex, uv)},
        {3, 0, VK_FORMAT_R16G16B16A16_UINT,   (uint32_t)offsetof(ModelVertex, joints0)},
        {4, 0, VK_FORMAT_R32G32B32A32_SFLOAT, (uint32_t)offsetof(ModelVertex, weights0)},
    };
    VkPipelineVertexInputStateCreateInfo vi = {VK_STRUCTURE_TYPE_PIPELINE_VERTEX_INPUT_STATE_CREATE_INFO};
    vi.vertexBindingDescriptionCount = 1;
    vi.pVertexBindingDescriptions = &vib;
    vi.vertexAttributeDescriptionCount = 5;
    vi.pVertexAttributeDescriptions = via;

    VkPipelineInputAssemblyStateCreateInfo ia = {VK_STRUCTURE_TYPE_PIPELINE_INPUT_ASSEMBLY_STATE_CREATE_INFO};
    ia.topology = VK_PRIMITIVE_TOPOLOGY_TRIANGLE_LIST;

    VkPipelineViewportStateCreateInfo vp = {VK_STRUCTURE_TYPE_PIPELINE_VIEWPORT_STATE_CREATE_INFO};
    vp.viewportCount = 1;
    vp.scissorCount = 1;

    VkPipelineRasterizationStateCreateInfo rs = {VK_STRUCTURE_TYPE_PIPELINE_RASTERIZATION_STATE_CREATE_INFO};
    rs.polygonMode = VK_POLYGON_MODE_FILL;
    rs.cullMode = VK_CULL_MODE_NONE;       // two-sided shading handles back faces
    rs.frontFace = VK_FRONT_FACE_COUNTER_CLOCKWISE;
    rs.lineWidth = 1.0f;

    VkPipelineMultisampleStateCreateInfo ms = {VK_STRUCTURE_TYPE_PIPELINE_MULTISAMPLE_STATE_CREATE_INFO};
    ms.rasterizationSamples = VK_SAMPLE_COUNT_1_BIT;

    VkPipelineDepthStencilStateCreateInfo ds = {VK_STRUCTURE_TYPE_PIPELINE_DEPTH_STENCIL_STATE_CREATE_INFO};
    ds.depthTestEnable = VK_TRUE;
    ds.depthWriteEnable = VK_TRUE;
    ds.depthCompareOp = VK_COMPARE_OP_LESS;

    VkPipelineColorBlendAttachmentState cba = {};
    cba.colorWriteMask = VK_COLOR_COMPONENT_R_BIT | VK_COLOR_COMPONENT_G_BIT |
                         VK_COLOR_COMPONENT_B_BIT | VK_COLOR_COMPONENT_A_BIT;
    cba.blendEnable = VK_FALSE;
    VkPipelineColorBlendStateCreateInfo cb = {VK_STRUCTURE_TYPE_PIPELINE_COLOR_BLEND_STATE_CREATE_INFO};
    cb.attachmentCount = 1;
    cb.pAttachments = &cba;

    VkDynamicState dynStates[2] = {VK_DYNAMIC_STATE_VIEWPORT, VK_DYNAMIC_STATE_SCISSOR};
    VkPipelineDynamicStateCreateInfo dyn = {VK_STRUCTURE_TYPE_PIPELINE_DYNAMIC_STATE_CREATE_INFO};
    dyn.dynamicStateCount = 2;
    dyn.pDynamicStates = dynStates;

    VkGraphicsPipelineCreateInfo gpci = {VK_STRUCTURE_TYPE_GRAPHICS_PIPELINE_CREATE_INFO};
    gpci.stageCount = 2;
    gpci.pStages = stages;
    gpci.pVertexInputState = &vi;
    gpci.pInputAssemblyState = &ia;
    gpci.pViewportState = &vp;
    gpci.pRasterizationState = &rs;
    gpci.pMultisampleState = &ms;
    gpci.pDepthStencilState = &ds;
    gpci.pColorBlendState = &cb;
    gpci.pDynamicState = &dyn;
    gpci.layout = pipelineLayout_;
    gpci.renderPass = renderPass_;
    gpci.subpass = 0;
    VkResult pr = vkCreateGraphicsPipelines(device_, VK_NULL_HANDLE, 1, &gpci, nullptr, &pipeline_);

    vkDestroyShaderModule(device_, vs, nullptr);
    vkDestroyShaderModule(device_, fs, nullptr);
    if (pr != VK_SUCCESS) {
        std::fprintf(stderr, "ModelRenderer: failed to create pipeline (%d)\n", pr);
        return false;
    }

    // Skybox pipeline: fullscreen.vert + skybox.frag, no vertex input, depth
    // test/write off (drawn behind the model). Reuses pipelineLayout_/renderPass_.
    {
        VkShaderModule svs = createShaderModule(device_, fullscreen_vert_data, sizeof(fullscreen_vert_data));
        VkShaderModule sfs = createShaderModule(device_, skybox_frag_data, sizeof(skybox_frag_data));
        VkPipelineShaderStageCreateInfo st2[2] = {};
        st2[0].sType = VK_STRUCTURE_TYPE_PIPELINE_SHADER_STAGE_CREATE_INFO; st2[0].stage = VK_SHADER_STAGE_VERTEX_BIT;   st2[0].module = svs; st2[0].pName = "main";
        st2[1].sType = VK_STRUCTURE_TYPE_PIPELINE_SHADER_STAGE_CREATE_INFO; st2[1].stage = VK_SHADER_STAGE_FRAGMENT_BIT; st2[1].module = sfs; st2[1].pName = "main";
        VkPipelineVertexInputStateCreateInfo vi2 = {VK_STRUCTURE_TYPE_PIPELINE_VERTEX_INPUT_STATE_CREATE_INFO};
        VkPipelineDepthStencilStateCreateInfo ds2 = {VK_STRUCTURE_TYPE_PIPELINE_DEPTH_STENCIL_STATE_CREATE_INFO};
        ds2.depthTestEnable = VK_FALSE; ds2.depthWriteEnable = VK_FALSE;
        VkGraphicsPipelineCreateInfo gp2 = gpci;   // reuse ia/vp/rs/ms/cb/dyn/layout/renderPass
        gp2.pStages = st2;
        gp2.pVertexInputState = &vi2;
        gp2.pDepthStencilState = &ds2;
        VkResult spr = vkCreateGraphicsPipelines(device_, VK_NULL_HANDLE, 1, &gp2, nullptr, &skyboxPipeline_);
        vkDestroyShaderModule(device_, svs, nullptr);
        vkDestroyShaderModule(device_, sfs, nullptr);
        if (spr != VK_SUCCESS) { std::fprintf(stderr, "ModelRenderer: failed to create skybox pipeline (%d)\n", spr); return false; }
    }

    // Descriptor pool + set + the host-visible uniform buffer.
    VkDescriptorPoolSize ps = {VK_DESCRIPTOR_TYPE_UNIFORM_BUFFER, 1};
    VkDescriptorPoolCreateInfo dpci = {VK_STRUCTURE_TYPE_DESCRIPTOR_POOL_CREATE_INFO};
    dpci.maxSets = 1;
    dpci.poolSizeCount = 1;
    dpci.pPoolSizes = &ps;
    if (vkCreateDescriptorPool(device_, &dpci, nullptr, &descriptorPool_) != VK_SUCCESS) return false;

    VkDescriptorSetAllocateInfo dsai = {VK_STRUCTURE_TYPE_DESCRIPTOR_SET_ALLOCATE_INFO};
    dsai.descriptorPool = descriptorPool_;
    dsai.descriptorSetCount = 1;
    dsai.pSetLayouts = &dsLayout_;
    if (vkAllocateDescriptorSets(device_, &dsai, &descriptorSet_) != VK_SUCCESS) return false;

    uniformBuffer_ = modelCreateBuffer(device_, physDevice_, sizeof(UniformBlock),
        VK_BUFFER_USAGE_UNIFORM_BUFFER_BIT,
        VK_MEMORY_PROPERTY_HOST_VISIBLE_BIT | VK_MEMORY_PROPERTY_HOST_COHERENT_BIT);
    if (uniformBuffer_.buffer == VK_NULL_HANDLE) return false;

    VkDescriptorBufferInfo dbi = {uniformBuffer_.buffer, 0, sizeof(UniformBlock)};
    VkWriteDescriptorSet w = {VK_STRUCTURE_TYPE_WRITE_DESCRIPTOR_SET};
    w.dstSet = descriptorSet_;
    w.dstBinding = 0;
    w.descriptorCount = 1;
    w.descriptorType = VK_DESCRIPTOR_TYPE_UNIFORM_BUFFER;
    w.pBufferInfo = &dbi;
    vkUpdateDescriptorSets(device_, 1, &w, 0, nullptr);
    return true;
}

ModelImage ModelRenderer::uploadTexture(const ModelTexture& tex) {
    ModelImage img;
    if (tex.width <= 0 || tex.height <= 0 || tex.rgba.empty()) return img;
    img.width = tex.width;
    img.height = tex.height;
    const uint32_t w = (uint32_t)tex.width, h = (uint32_t)tex.height;
    const uint32_t mips = (uint32_t)std::floor(std::log2((float)std::max(w, h))) + 1u;

    VkImageCreateInfo ici = {VK_STRUCTURE_TYPE_IMAGE_CREATE_INFO};
    ici.imageType = VK_IMAGE_TYPE_2D;
    ici.format = VK_FORMAT_R8G8B8A8_UNORM;  // sRGB decode done in the shader
    ici.extent = {w, h, 1};
    ici.mipLevels = mips;
    ici.arrayLayers = 1;
    ici.samples = VK_SAMPLE_COUNT_1_BIT;
    ici.tiling = VK_IMAGE_TILING_OPTIMAL;
    ici.usage = VK_IMAGE_USAGE_SAMPLED_BIT | VK_IMAGE_USAGE_TRANSFER_DST_BIT |
                VK_IMAGE_USAGE_TRANSFER_SRC_BIT;  // SRC for mip blits
    ici.initialLayout = VK_IMAGE_LAYOUT_UNDEFINED;
    if (vkCreateImage(device_, &ici, nullptr, &img.image) != VK_SUCCESS) return ModelImage{};

    VkMemoryRequirements mr;
    vkGetImageMemoryRequirements(device_, img.image, &mr);
    VkMemoryAllocateInfo ai = {VK_STRUCTURE_TYPE_MEMORY_ALLOCATE_INFO};
    ai.allocationSize = mr.size;
    ai.memoryTypeIndex = modelFindMemoryType(physDevice_, mr.memoryTypeBits, VK_MEMORY_PROPERTY_DEVICE_LOCAL_BIT);
    if (vkAllocateMemory(device_, &ai, nullptr, &img.memory) != VK_SUCCESS) { vkDestroyImage(device_, img.image, nullptr); return ModelImage{}; }
    vkBindImageMemory(device_, img.image, img.memory, 0);

    VkImageViewCreateInfo vci = {VK_STRUCTURE_TYPE_IMAGE_VIEW_CREATE_INFO};
    vci.image = img.image;
    vci.viewType = VK_IMAGE_VIEW_TYPE_2D;
    vci.format = ici.format;
    vci.subresourceRange = {VK_IMAGE_ASPECT_COLOR_BIT, 0, mips, 0, 1};
    vkCreateImageView(device_, &vci, nullptr, &img.view);

    // Staging upload of mip 0.
    ModelBuffer staging = modelCreateBuffer(device_, physDevice_, tex.rgba.size(),
        VK_BUFFER_USAGE_TRANSFER_SRC_BIT,
        VK_MEMORY_PROPERTY_HOST_VISIBLE_BIT | VK_MEMORY_PROPERTY_HOST_COHERENT_BIT);
    void* mapped = nullptr;
    vkMapMemory(device_, staging.memory, 0, tex.rgba.size(), 0, &mapped);
    std::memcpy(mapped, tex.rgba.data(), tex.rgba.size());
    vkUnmapMemory(device_, staging.memory);

    VkCommandBufferAllocateInfo cbai = {VK_STRUCTURE_TYPE_COMMAND_BUFFER_ALLOCATE_INFO};
    cbai.commandPool = cmdPool_; cbai.level = VK_COMMAND_BUFFER_LEVEL_PRIMARY; cbai.commandBufferCount = 1;
    VkCommandBuffer cmd; vkAllocateCommandBuffers(device_, &cbai, &cmd);
    VkCommandBufferBeginInfo bi = {VK_STRUCTURE_TYPE_COMMAND_BUFFER_BEGIN_INFO};
    bi.flags = VK_COMMAND_BUFFER_USAGE_ONE_TIME_SUBMIT_BIT;
    vkBeginCommandBuffer(cmd, &bi);

    auto barrier = [&](uint32_t mip, uint32_t count, VkImageLayout oldL, VkImageLayout newL,
                       VkAccessFlags srcA, VkAccessFlags dstA, VkPipelineStageFlags srcS, VkPipelineStageFlags dstS) {
        VkImageMemoryBarrier b = {VK_STRUCTURE_TYPE_IMAGE_MEMORY_BARRIER};
        b.oldLayout = oldL; b.newLayout = newL;
        b.srcAccessMask = srcA; b.dstAccessMask = dstA;
        b.srcQueueFamilyIndex = VK_QUEUE_FAMILY_IGNORED; b.dstQueueFamilyIndex = VK_QUEUE_FAMILY_IGNORED;
        b.image = img.image;
        b.subresourceRange = {VK_IMAGE_ASPECT_COLOR_BIT, mip, count, 0, 1};
        vkCmdPipelineBarrier(cmd, srcS, dstS, 0, 0, nullptr, 0, nullptr, 1, &b);
    };

    // mip 0 → TRANSFER_DST, copy.
    barrier(0, 1, VK_IMAGE_LAYOUT_UNDEFINED, VK_IMAGE_LAYOUT_TRANSFER_DST_OPTIMAL,
            0, VK_ACCESS_TRANSFER_WRITE_BIT, VK_PIPELINE_STAGE_TOP_OF_PIPE_BIT, VK_PIPELINE_STAGE_TRANSFER_BIT);
    VkBufferImageCopy region = {};
    region.imageSubresource = {VK_IMAGE_ASPECT_COLOR_BIT, 0, 0, 1};
    region.imageExtent = {w, h, 1};
    vkCmdCopyBufferToImage(cmd, staging.buffer, img.image, VK_IMAGE_LAYOUT_TRANSFER_DST_OPTIMAL, 1, &region);

    // Generate mips by successive blits.
    int32_t mw = (int32_t)w, mh = (int32_t)h;
    for (uint32_t i = 1; i < mips; ++i) {
        // src mip (i-1): TRANSFER_DST → TRANSFER_SRC
        barrier(i - 1, 1, VK_IMAGE_LAYOUT_TRANSFER_DST_OPTIMAL, VK_IMAGE_LAYOUT_TRANSFER_SRC_OPTIMAL,
                VK_ACCESS_TRANSFER_WRITE_BIT, VK_ACCESS_TRANSFER_READ_BIT, VK_PIPELINE_STAGE_TRANSFER_BIT, VK_PIPELINE_STAGE_TRANSFER_BIT);
        // dst mip (i): UNDEFINED → TRANSFER_DST
        barrier(i, 1, VK_IMAGE_LAYOUT_UNDEFINED, VK_IMAGE_LAYOUT_TRANSFER_DST_OPTIMAL,
                0, VK_ACCESS_TRANSFER_WRITE_BIT, VK_PIPELINE_STAGE_TRANSFER_BIT, VK_PIPELINE_STAGE_TRANSFER_BIT);
        int32_t nw = mw > 1 ? mw / 2 : 1, nh = mh > 1 ? mh / 2 : 1;
        VkImageBlit blit = {};
        blit.srcSubresource = {VK_IMAGE_ASPECT_COLOR_BIT, i - 1, 0, 1};
        blit.srcOffsets[1] = {mw, mh, 1};
        blit.dstSubresource = {VK_IMAGE_ASPECT_COLOR_BIT, i, 0, 1};
        blit.dstOffsets[1] = {nw, nh, 1};
        vkCmdBlitImage(cmd, img.image, VK_IMAGE_LAYOUT_TRANSFER_SRC_OPTIMAL,
                       img.image, VK_IMAGE_LAYOUT_TRANSFER_DST_OPTIMAL, 1, &blit, VK_FILTER_LINEAR);
        // src mip (i-1) → SHADER_READ
        barrier(i - 1, 1, VK_IMAGE_LAYOUT_TRANSFER_SRC_OPTIMAL, VK_IMAGE_LAYOUT_SHADER_READ_ONLY_OPTIMAL,
                VK_ACCESS_TRANSFER_READ_BIT, VK_ACCESS_SHADER_READ_BIT, VK_PIPELINE_STAGE_TRANSFER_BIT, VK_PIPELINE_STAGE_FRAGMENT_SHADER_BIT);
        mw = nw; mh = nh;
    }
    // last mip → SHADER_READ
    barrier(mips - 1, 1, VK_IMAGE_LAYOUT_TRANSFER_DST_OPTIMAL, VK_IMAGE_LAYOUT_SHADER_READ_ONLY_OPTIMAL,
            VK_ACCESS_TRANSFER_WRITE_BIT, VK_ACCESS_SHADER_READ_BIT, VK_PIPELINE_STAGE_TRANSFER_BIT, VK_PIPELINE_STAGE_FRAGMENT_SHADER_BIT);

    vkEndCommandBuffer(cmd);
    VkSubmitInfo si = {VK_STRUCTURE_TYPE_SUBMIT_INFO};
    si.commandBufferCount = 1; si.pCommandBuffers = &cmd;
    vkQueueSubmit(queue_, 1, &si, VK_NULL_HANDLE);
    vkQueueWaitIdle(queue_);
    vkFreeCommandBuffers(device_, cmdPool_, 1, &cmd);
    modelDestroyBuffer(device_, staging);
    return img;
}

bool ModelRenderer::createSamplerAndDefaults() {
    VkSamplerCreateInfo sci = {VK_STRUCTURE_TYPE_SAMPLER_CREATE_INFO};
    sci.magFilter = VK_FILTER_LINEAR;
    sci.minFilter = VK_FILTER_LINEAR;
    sci.mipmapMode = VK_SAMPLER_MIPMAP_MODE_LINEAR;
    sci.addressModeU = VK_SAMPLER_ADDRESS_MODE_REPEAT;
    sci.addressModeV = VK_SAMPLER_ADDRESS_MODE_REPEAT;
    sci.addressModeW = VK_SAMPLER_ADDRESS_MODE_REPEAT;
    sci.maxLod = VK_LOD_CLAMP_NONE;
    sci.borderColor = VK_BORDER_COLOR_FLOAT_OPAQUE_WHITE;
    if (vkCreateSampler(device_, &sci, nullptr, &sampler_) != VK_SUCCESS) return false;

    ModelTexture white; white.width = 1; white.height = 1; white.rgba = {255, 255, 255, 255};
    whiteTex_ = uploadTexture(white);
    ModelTexture flat;  flat.width = 1;  flat.height = 1;  flat.rgba = {128, 128, 255, 255};
    flatNormalTex_ = uploadTexture(flat);
    return whiteTex_.view != VK_NULL_HANDLE && flatNormalTex_.view != VK_NULL_HANDLE;
}

bool ModelRenderer::genCubeMap(CubeMap& cube, uint32_t size, uint32_t mips,
                               const uint32_t* fragSpv, size_t fragSpvBytes, bool /*perMipRoughness*/) {
    cube.size = size; cube.mips = mips;
    const VkFormat fmt = VK_FORMAT_R16G16B16A16_SFLOAT;

    VkImageCreateInfo ici = {VK_STRUCTURE_TYPE_IMAGE_CREATE_INFO};
    ici.flags = VK_IMAGE_CREATE_CUBE_COMPATIBLE_BIT;
    ici.imageType = VK_IMAGE_TYPE_2D;
    ici.format = fmt;
    ici.extent = {size, size, 1};
    ici.mipLevels = mips;
    ici.arrayLayers = 6;
    ici.samples = VK_SAMPLE_COUNT_1_BIT;
    ici.tiling = VK_IMAGE_TILING_OPTIMAL;
    ici.usage = VK_IMAGE_USAGE_COLOR_ATTACHMENT_BIT | VK_IMAGE_USAGE_SAMPLED_BIT;
    ici.initialLayout = VK_IMAGE_LAYOUT_UNDEFINED;
    if (vkCreateImage(device_, &ici, nullptr, &cube.image) != VK_SUCCESS) return false;
    VkMemoryRequirements mreq; vkGetImageMemoryRequirements(device_, cube.image, &mreq);
    VkMemoryAllocateInfo ai = {VK_STRUCTURE_TYPE_MEMORY_ALLOCATE_INFO};
    ai.allocationSize = mreq.size;
    ai.memoryTypeIndex = modelFindMemoryType(physDevice_, mreq.memoryTypeBits, VK_MEMORY_PROPERTY_DEVICE_LOCAL_BIT);
    if (vkAllocateMemory(device_, &ai, nullptr, &cube.memory) != VK_SUCCESS) return false;
    vkBindImageMemory(device_, cube.image, cube.memory, 0);

    VkImageViewCreateInfo cvci = {VK_STRUCTURE_TYPE_IMAGE_VIEW_CREATE_INFO};
    cvci.image = cube.image; cvci.viewType = VK_IMAGE_VIEW_TYPE_CUBE; cvci.format = fmt;
    cvci.subresourceRange = {VK_IMAGE_ASPECT_COLOR_BIT, 0, mips, 0, 6};
    if (vkCreateImageView(device_, &cvci, nullptr, &cube.view) != VK_SUCCESS) return false;

    struct Push { int32_t face; float roughness; };
    FsPipe fp = makeFsPipe(device_, fmt, fragSpv, fragSpvBytes, sizeof(Push));

    for (uint32_t m = 0; m < mips; ++m) {
        uint32_t ms = size >> m; if (ms == 0) ms = 1;
        float rough = (mips > 1) ? (float)m / (float)(mips - 1) : 0.0f;
        for (uint32_t f = 0; f < 6; ++f) {
            VkImageView faceView;
            VkImageViewCreateInfo fv = {VK_STRUCTURE_TYPE_IMAGE_VIEW_CREATE_INFO};
            fv.image = cube.image; fv.viewType = VK_IMAGE_VIEW_TYPE_2D; fv.format = fmt;
            fv.subresourceRange = {VK_IMAGE_ASPECT_COLOR_BIT, m, 1, f, 1};
            vkCreateImageView(device_, &fv, nullptr, &faceView);

            VkFramebufferCreateInfo fbci = {VK_STRUCTURE_TYPE_FRAMEBUFFER_CREATE_INFO};
            fbci.renderPass = fp.rp; fbci.attachmentCount = 1; fbci.pAttachments = &faceView;
            fbci.width = ms; fbci.height = ms; fbci.layers = 1;
            VkFramebuffer fb; vkCreateFramebuffer(device_, &fbci, nullptr, &fb);

            VkCommandBufferAllocateInfo cai = {VK_STRUCTURE_TYPE_COMMAND_BUFFER_ALLOCATE_INFO};
            cai.commandPool = cmdPool_; cai.level = VK_COMMAND_BUFFER_LEVEL_PRIMARY; cai.commandBufferCount = 1;
            VkCommandBuffer cmd; vkAllocateCommandBuffers(device_, &cai, &cmd);
            VkCommandBufferBeginInfo bi = {VK_STRUCTURE_TYPE_COMMAND_BUFFER_BEGIN_INFO};
            bi.flags = VK_COMMAND_BUFFER_USAGE_ONE_TIME_SUBMIT_BIT;
            vkBeginCommandBuffer(cmd, &bi);
            VkRenderPassBeginInfo rpbi = {VK_STRUCTURE_TYPE_RENDER_PASS_BEGIN_INFO};
            rpbi.renderPass = fp.rp; rpbi.framebuffer = fb;
            rpbi.renderArea.extent = {ms, ms};
            vkCmdBeginRenderPass(cmd, &rpbi, VK_SUBPASS_CONTENTS_INLINE);
            VkViewport vpr = {0, 0, (float)ms, (float)ms, 0, 1};
            VkRect2D sc = {{0, 0}, {ms, ms}};
            vkCmdSetViewport(cmd, 0, 1, &vpr); vkCmdSetScissor(cmd, 0, 1, &sc);
            vkCmdBindPipeline(cmd, VK_PIPELINE_BIND_POINT_GRAPHICS, fp.pipe);
            Push pcv = {(int32_t)f, rough};
            vkCmdPushConstants(cmd, fp.layout, VK_SHADER_STAGE_FRAGMENT_BIT, 0, sizeof(Push), &pcv);
            vkCmdDraw(cmd, 3, 1, 0, 0);
            vkCmdEndRenderPass(cmd);
            vkEndCommandBuffer(cmd);
            VkSubmitInfo si = {VK_STRUCTURE_TYPE_SUBMIT_INFO}; si.commandBufferCount = 1; si.pCommandBuffers = &cmd;
            vkQueueSubmit(queue_, 1, &si, VK_NULL_HANDLE); vkQueueWaitIdle(queue_);
            vkFreeCommandBuffers(device_, cmdPool_, 1, &cmd);
            vkDestroyFramebuffer(device_, fb, nullptr);
            vkDestroyImageView(device_, faceView, nullptr);
        }
    }
    destroyFsPipe(device_, fp);
    return true;
}

bool ModelRenderer::createIbl() {
    VkSamplerCreateInfo sc = {VK_STRUCTURE_TYPE_SAMPLER_CREATE_INFO};
    sc.magFilter = VK_FILTER_LINEAR; sc.minFilter = VK_FILTER_LINEAR;
    sc.mipmapMode = VK_SAMPLER_MIPMAP_MODE_LINEAR;
    sc.addressModeU = VK_SAMPLER_ADDRESS_MODE_CLAMP_TO_EDGE;
    sc.addressModeV = VK_SAMPLER_ADDRESS_MODE_CLAMP_TO_EDGE;
    sc.addressModeW = VK_SAMPLER_ADDRESS_MODE_CLAMP_TO_EDGE;
    sc.maxLod = VK_LOD_CLAMP_NONE; sc.borderColor = VK_BORDER_COLOR_FLOAT_OPAQUE_WHITE;
    if (vkCreateSampler(device_, &sc, nullptr, &iblCubeSampler_) != VK_SUCCESS) return false;
    VkSamplerCreateInfo sl = sc; sl.maxLod = 0.0f;
    if (vkCreateSampler(device_, &sl, nullptr, &iblLutSampler_) != VK_SUCCESS) return false;

    // BRDF LUT (2D R16G16) via a fullscreen pass.
    brdfLut_ = modelCreateImage2D(device_, physDevice_, 256, 256, VK_FORMAT_R16G16_SFLOAT,
        VK_IMAGE_USAGE_COLOR_ATTACHMENT_BIT | VK_IMAGE_USAGE_SAMPLED_BIT);
    if (brdfLut_.image == VK_NULL_HANDLE) return false;
    {
        FsPipe fp = makeFsPipe(device_, VK_FORMAT_R16G16_SFLOAT, brdf_lut_frag_data, sizeof(brdf_lut_frag_data), 0);
        VkFramebufferCreateInfo fbci = {VK_STRUCTURE_TYPE_FRAMEBUFFER_CREATE_INFO};
        fbci.renderPass = fp.rp; fbci.attachmentCount = 1; fbci.pAttachments = &brdfLut_.view;
        fbci.width = 256; fbci.height = 256; fbci.layers = 1;
        VkFramebuffer fb; vkCreateFramebuffer(device_, &fbci, nullptr, &fb);
        VkCommandBufferAllocateInfo cai = {VK_STRUCTURE_TYPE_COMMAND_BUFFER_ALLOCATE_INFO};
        cai.commandPool = cmdPool_; cai.level = VK_COMMAND_BUFFER_LEVEL_PRIMARY; cai.commandBufferCount = 1;
        VkCommandBuffer cmd; vkAllocateCommandBuffers(device_, &cai, &cmd);
        VkCommandBufferBeginInfo bi = {VK_STRUCTURE_TYPE_COMMAND_BUFFER_BEGIN_INFO};
        bi.flags = VK_COMMAND_BUFFER_USAGE_ONE_TIME_SUBMIT_BIT;
        vkBeginCommandBuffer(cmd, &bi);
        VkRenderPassBeginInfo rpbi = {VK_STRUCTURE_TYPE_RENDER_PASS_BEGIN_INFO};
        rpbi.renderPass = fp.rp; rpbi.framebuffer = fb; rpbi.renderArea.extent = {256, 256};
        vkCmdBeginRenderPass(cmd, &rpbi, VK_SUBPASS_CONTENTS_INLINE);
        VkViewport vpr = {0, 0, 256, 256, 0, 1}; VkRect2D scs = {{0, 0}, {256, 256}};
        vkCmdSetViewport(cmd, 0, 1, &vpr); vkCmdSetScissor(cmd, 0, 1, &scs);
        vkCmdBindPipeline(cmd, VK_PIPELINE_BIND_POINT_GRAPHICS, fp.pipe);
        vkCmdDraw(cmd, 3, 1, 0, 0);
        vkCmdEndRenderPass(cmd); vkEndCommandBuffer(cmd);
        VkSubmitInfo si = {VK_STRUCTURE_TYPE_SUBMIT_INFO}; si.commandBufferCount = 1; si.pCommandBuffers = &cmd;
        vkQueueSubmit(queue_, 1, &si, VK_NULL_HANDLE); vkQueueWaitIdle(queue_);
        vkFreeCommandBuffers(device_, cmdPool_, 1, &cmd);
        vkDestroyFramebuffer(device_, fb, nullptr);
        destroyFsPipe(device_, fp);
    }

    if (!genCubeMap(irradianceCube_, 32, 1, irradiance_frag_data, sizeof(irradiance_frag_data), false)) return false;
    if (!genCubeMap(prefilterCube_, 128, 5, prefilter_frag_data, sizeof(prefilter_frag_data), true)) return false;

    // Descriptor set (set = 2).
    VkDescriptorPoolSize ps = {VK_DESCRIPTOR_TYPE_COMBINED_IMAGE_SAMPLER, 3};
    VkDescriptorPoolCreateInfo dpci = {VK_STRUCTURE_TYPE_DESCRIPTOR_POOL_CREATE_INFO};
    dpci.maxSets = 1; dpci.poolSizeCount = 1; dpci.pPoolSizes = &ps;
    if (vkCreateDescriptorPool(device_, &dpci, nullptr, &iblPool_) != VK_SUCCESS) return false;
    VkDescriptorSetAllocateInfo dsai = {VK_STRUCTURE_TYPE_DESCRIPTOR_SET_ALLOCATE_INFO};
    dsai.descriptorPool = iblPool_; dsai.descriptorSetCount = 1; dsai.pSetLayouts = &iblSetLayout_;
    if (vkAllocateDescriptorSets(device_, &dsai, &iblSet_) != VK_SUCCESS) return false;
    VkDescriptorImageInfo ii[3] = {
        {iblCubeSampler_, irradianceCube_.view, VK_IMAGE_LAYOUT_SHADER_READ_ONLY_OPTIMAL},
        {iblCubeSampler_, prefilterCube_.view,  VK_IMAGE_LAYOUT_SHADER_READ_ONLY_OPTIMAL},
        {iblLutSampler_,  brdfLut_.view,        VK_IMAGE_LAYOUT_SHADER_READ_ONLY_OPTIMAL},
    };
    VkWriteDescriptorSet w[3];
    for (uint32_t i = 0; i < 3; ++i) {
        w[i] = {VK_STRUCTURE_TYPE_WRITE_DESCRIPTOR_SET};
        w[i].dstSet = iblSet_; w[i].dstBinding = i; w[i].descriptorCount = 1;
        w[i].descriptorType = VK_DESCRIPTOR_TYPE_COMBINED_IMAGE_SAMPLER; w[i].pImageInfo = &ii[i];
    }
    vkUpdateDescriptorSets(device_, 3, w, 0, nullptr);
    std::printf("ModelRenderer: IBL ready (irradiance 32, prefilter 128x%u, BRDF LUT 256)\n", prefilterCube_.mips);
    return true;
}

VkDescriptorSet ModelRenderer::makeMaterialSet(VkImageView baseColor, VkImageView mr,
                                               VkImageView normal, VkImageView occ,
                                               VkImageView emissive) {
    VkDescriptorSetAllocateInfo ai = {VK_STRUCTURE_TYPE_DESCRIPTOR_SET_ALLOCATE_INFO};
    ai.descriptorPool = matPool_;
    ai.descriptorSetCount = 1;
    ai.pSetLayouts = &matSetLayout_;
    VkDescriptorSet set = VK_NULL_HANDLE;
    if (vkAllocateDescriptorSets(device_, &ai, &set) != VK_SUCCESS) return VK_NULL_HANDLE;

    VkImageView views[5] = {baseColor, mr, normal, occ, emissive};
    VkDescriptorImageInfo infos[5];
    VkWriteDescriptorSet writes[5];
    for (uint32_t i = 0; i < 5; ++i) {
        infos[i] = {sampler_, views[i], VK_IMAGE_LAYOUT_SHADER_READ_ONLY_OPTIMAL};
        writes[i] = {VK_STRUCTURE_TYPE_WRITE_DESCRIPTOR_SET};
        writes[i].dstSet = set;
        writes[i].dstBinding = i;
        writes[i].descriptorCount = 1;
        writes[i].descriptorType = VK_DESCRIPTOR_TYPE_COMBINED_IMAGE_SAMPLER;
        writes[i].pImageInfo = &infos[i];
    }
    vkUpdateDescriptorSets(device_, 5, writes, 0, nullptr);
    return set;
}

bool ModelRenderer::finalizeModel(ModelData& md) {
    cleanupModel();

    // Morph models need a CPU-writable vertex buffer (re-blended each frame);
    // everything else keeps the device-local staged upload.
    hasMorph_ = !md.morphs.empty();
    const VkDeviceSize vbSize = md.vertices.size() * sizeof(ModelVertex);
    vertexBuffer_ = modelCreateBuffer(device_, physDevice_, vbSize,
        VK_BUFFER_USAGE_VERTEX_BUFFER_BIT | VK_BUFFER_USAGE_TRANSFER_DST_BIT,
        hasMorph_ ? (VK_MEMORY_PROPERTY_HOST_VISIBLE_BIT | VK_MEMORY_PROPERTY_HOST_COHERENT_BIT)
                  : VK_MEMORY_PROPERTY_DEVICE_LOCAL_BIT);
    indexBuffer_ = modelCreateBuffer(device_, physDevice_,
        md.indices.size() * sizeof(uint32_t),
        VK_BUFFER_USAGE_INDEX_BUFFER_BIT | VK_BUFFER_USAGE_TRANSFER_DST_BIT,
        VK_MEMORY_PROPERTY_DEVICE_LOCAL_BIT);
    if (vertexBuffer_.buffer == VK_NULL_HANDLE || indexBuffer_.buffer == VK_NULL_HANDLE) return false;
    if (hasMorph_) {
        // Keep the base verts for re-blending; seed the buffer with them.
        morphBase_ = md.vertices;
        void* mapped = nullptr;
        vkMapMemory(device_, vertexBuffer_.memory, 0, vbSize, 0, &mapped);
        std::memcpy(mapped, md.vertices.data(), (size_t)vbSize);
        vkUnmapMemory(device_, vertexBuffer_.memory);
    } else {
        modelUploadBuffer(device_, physDevice_, queue_, cmdPool_, vertexBuffer_,
                          md.vertices.data(), vertexBuffer_.size);
    }
    modelUploadBuffer(device_, physDevice_, queue_, cmdPool_, indexBuffer_,
                      md.indices.data(), indexBuffer_.size);

    // Upload textures (parallel to md.textures; empty entries stay null).
    modelTextures_.resize(md.textures.size());
    for (size_t i = 0; i < md.textures.size(); ++i)
        if (!md.textures[i].rgba.empty())
            modelTextures_[i] = uploadTexture(md.textures[i]);

    materials_ = std::move(md.materials);
    primitives_ = std::move(md.primitives);

    // Animation graph (Phase 1). Auto-play the first clip; no clips → static
    // fast-path (activeAnim_ stays -1 and updateAnimation is a no-op).
    nodes_ = std::move(md.nodes);
    animations_ = std::move(md.animations);
    rootNodes_ = std::move(md.rootNodes);
    morphs_ = std::move(md.morphs);
    bindNodes_ = nodes_;                  // bind-pose snapshot for clean clip switches
    nodeWorld_.assign(nodes_.size() * 16, 0.0f);
    activeAnim_ = animations_.empty() ? -1 : 0;
    animTime_ = 0.0f;
    paused_ = false;

    // ── Skinning: joint-matrix SSBO (set = 3) ────────────────────────────────
    // Sized to all skins' joints concatenated; ≥1 matrix so static models still
    // bind a valid set 3. Host-visible: updateAnimation memcpy's it each frame.
    // Initialized to identity — the static fast-path then leaves it untouched.
    skins_ = std::move(md.skins);
    jointCount_ = md.totalJoints > 0 ? md.totalJoints : 1;
    jointBuffer_ = modelCreateBuffer(device_, physDevice_,
        (VkDeviceSize)jointCount_ * 16 * sizeof(float),
        VK_BUFFER_USAGE_STORAGE_BUFFER_BIT,
        VK_MEMORY_PROPERTY_HOST_VISIBLE_BIT | VK_MEMORY_PROPERTY_HOST_COHERENT_BIT);
    if (jointBuffer_.buffer == VK_NULL_HANDLE) return false;
    {
        void* mapped = nullptr;
        vkMapMemory(device_, jointBuffer_.memory, 0, jointBuffer_.size, 0, &mapped);
        const glm::mat4 ident(1.0f);
        for (uint32_t i = 0; i < jointCount_; ++i)
            std::memcpy((char*)mapped + i * 16 * sizeof(float), glm::value_ptr(ident), 16 * sizeof(float));
        vkUnmapMemory(device_, jointBuffer_.memory);
    }
    {
        VkDescriptorPoolSize jps = {VK_DESCRIPTOR_TYPE_STORAGE_BUFFER, 1};
        VkDescriptorPoolCreateInfo jdpci = {VK_STRUCTURE_TYPE_DESCRIPTOR_POOL_CREATE_INFO};
        jdpci.maxSets = 1;
        jdpci.poolSizeCount = 1;
        jdpci.pPoolSizes = &jps;
        if (vkCreateDescriptorPool(device_, &jdpci, nullptr, &jointPool_) != VK_SUCCESS) return false;
        VkDescriptorSetAllocateInfo jdsai = {VK_STRUCTURE_TYPE_DESCRIPTOR_SET_ALLOCATE_INFO};
        jdsai.descriptorPool = jointPool_;
        jdsai.descriptorSetCount = 1;
        jdsai.pSetLayouts = &jointSetLayout_;
        if (vkAllocateDescriptorSets(device_, &jdsai, &jointSet_) != VK_SUCCESS) return false;
        VkDescriptorBufferInfo jdbi = {jointBuffer_.buffer, 0, jointBuffer_.size};
        VkWriteDescriptorSet jw = {VK_STRUCTURE_TYPE_WRITE_DESCRIPTOR_SET};
        jw.dstSet = jointSet_;
        jw.dstBinding = 0;
        jw.descriptorCount = 1;
        jw.descriptorType = VK_DESCRIPTOR_TYPE_STORAGE_BUFFER;
        jw.pBufferInfo = &jdbi;
        vkUpdateDescriptorSets(device_, 1, &jw, 0, nullptr);
    }

    std::memcpy(bboxMin_, md.bboxMin, sizeof(bboxMin_));
    std::memcpy(bboxMax_, md.bboxMax, sizeof(bboxMax_));
    hasBBox_ = md.hasBBox;
    numPrimitives_ = md.primitiveCount;

    // For animated models, replace the bind-pose AABB with the swept animated
    // bounds (correct height/center for the auto-fit). Needs primitives_/nodes_/
    // animations_/skins_ (set above) plus the still-intact CPU verts + indices.
    recomputeAnimatedBounds(md.vertices, md.indices);

    // Apply the rest-pose morph once so a model with nonzero default weights and
    // no clip still shows its morphed shape (animated models re-blend per frame).
    blendMorphs();

    // Per-material descriptor sets (set = 1) + a default for material == -1.
    const uint32_t nSets = (uint32_t)materials_.size() + 1;
    VkDescriptorPoolSize ps = {VK_DESCRIPTOR_TYPE_COMBINED_IMAGE_SAMPLER, nSets * 5};
    VkDescriptorPoolCreateInfo dpci = {VK_STRUCTURE_TYPE_DESCRIPTOR_POOL_CREATE_INFO};
    dpci.maxSets = nSets;
    dpci.poolSizeCount = 1;
    dpci.pPoolSizes = &ps;
    if (vkCreateDescriptorPool(device_, &dpci, nullptr, &matPool_) != VK_SUCCESS) return false;

    auto viewOr = [&](int idx, VkImageView def) -> VkImageView {
        if (idx >= 0 && idx < (int)modelTextures_.size() && modelTextures_[idx].view != VK_NULL_HANDLE)
            return modelTextures_[idx].view;
        return def;
    };
    materialSets_.reserve(materials_.size());
    for (const auto& m : materials_) {
        materialSets_.push_back(makeMaterialSet(
            viewOr(m.baseColorTex, whiteTex_.view),
            viewOr(m.metallicRoughnessTex, whiteTex_.view),
            viewOr(m.normalTex, flatNormalTex_.view),
            viewOr(m.occlusionTex, whiteTex_.view),
            viewOr(m.emissiveTex, whiteTex_.view)));
    }
    defaultMatSet_ = makeMaterialSet(whiteTex_.view, whiteTex_.view, flatNormalTex_.view,
                                     whiteTex_.view, whiteTex_.view);

    modelLoaded_ = true;
    return true;
}

