// clang-format on


bool
comp_multi_content_blend_init(struct comp_multi_content_blend *blend, struct vk_bundle *vk, VkFormat atlas_fmt)
{
	VkResult ret;

	VkShaderModuleCreateInfo vert_ci = {
	    .sType = VK_STRUCTURE_TYPE_SHADER_MODULE_CREATE_INFO,
	    .codeSize = sizeof(content_vert_spv),
	    .pCode = content_vert_spv,
	};
	ret = vk->vkCreateShaderModule(vk->device, &vert_ci, NULL, &blend->vert_mod);
	if (ret != VK_SUCCESS) {
		U_LOG_E("[content blend] Failed to create vert shader: %d", ret);
		return false;
	}

	VkShaderModuleCreateInfo frag_ci = {
	    .sType = VK_STRUCTURE_TYPE_SHADER_MODULE_CREATE_INFO,
	    .codeSize = sizeof(content_frag_spv),
	    .pCode = content_frag_spv,
	};
	ret = vk->vkCreateShaderModule(vk->device, &frag_ci, NULL, &blend->frag_mod);
	if (ret != VK_SUCCESS) {
		U_LOG_E("[content blend] Failed to create frag shader: %d", ret);
		return false;
	}

	// Linear sampler — source sub-rect rarely aligns to dst pixels.
	VkSamplerCreateInfo samp_ci = {
	    .sType = VK_STRUCTURE_TYPE_SAMPLER_CREATE_INFO,
	    .magFilter = VK_FILTER_LINEAR,
	    .minFilter = VK_FILTER_LINEAR,
	    .addressModeU = VK_SAMPLER_ADDRESS_MODE_CLAMP_TO_EDGE,
	    .addressModeV = VK_SAMPLER_ADDRESS_MODE_CLAMP_TO_EDGE,
	};
	ret = vk->vkCreateSampler(vk->device, &samp_ci, NULL, &blend->sampler);
	if (ret != VK_SUCCESS) {
		U_LOG_E("[content blend] Failed to create sampler: %d", ret);
		return false;
	}

	// Binding 0 = content sampler, binding 1 = HUD sampler (the
	// XR_EXT_window_space_layer source the fragment shader composites OVER the
	// content). Both fragment-stage combined image samplers.
	VkDescriptorSetLayoutBinding bindings[2] = {
	    {
	        .binding = 0,
	        .descriptorType = VK_DESCRIPTOR_TYPE_COMBINED_IMAGE_SAMPLER,
	        .descriptorCount = 1,
	        .stageFlags = VK_SHADER_STAGE_FRAGMENT_BIT,
	    },
	    {
	        .binding = 1,
	        .descriptorType = VK_DESCRIPTOR_TYPE_COMBINED_IMAGE_SAMPLER,
	        .descriptorCount = 1,
	        .stageFlags = VK_SHADER_STAGE_FRAGMENT_BIT,
	    },
	};
	VkDescriptorSetLayoutCreateInfo dsl_ci = {
	    .sType = VK_STRUCTURE_TYPE_DESCRIPTOR_SET_LAYOUT_CREATE_INFO,
	    .bindingCount = 2,
	    .pBindings = bindings,
	};
	ret = vk->vkCreateDescriptorSetLayout(vk->device, &dsl_ci, NULL, &blend->desc_layout);
	if (ret != VK_SUCCESS) {
		return false;
	}

	// Pipeline layout with the fragment push-constant block.
	VkPushConstantRange pc_range = {
	    .stageFlags = VK_SHADER_STAGE_FRAGMENT_BIT,
	    .offset = 0,
	    .size = sizeof(struct comp_multi_content_pc),
	};
	VkPipelineLayoutCreateInfo pl_ci = {
	    .sType = VK_STRUCTURE_TYPE_PIPELINE_LAYOUT_CREATE_INFO,
	    .setLayoutCount = 1,
	    .pSetLayouts = &blend->desc_layout,
	    .pushConstantRangeCount = 1,
	    .pPushConstantRanges = &pc_range,
	};
	ret = vk->vkCreatePipelineLayout(vk->device, &pl_ci, NULL, &blend->pipe_layout);
	if (ret != VK_SUCCESS) {
		return false;
	}

	// Two combined image samplers per set (content + HUD) across MAX_DESCS sets.
	VkDescriptorPoolSize pool_size = {
	    VK_DESCRIPTOR_TYPE_COMBINED_IMAGE_SAMPLER,
	    2 * COMP_MULTI_CONTENT_BLEND_MAX_DESCS,
	};
	VkDescriptorPoolCreateInfo dp_ci = {
	    .sType = VK_STRUCTURE_TYPE_DESCRIPTOR_POOL_CREATE_INFO,
	    .maxSets = COMP_MULTI_CONTENT_BLEND_MAX_DESCS,
	    .poolSizeCount = 1,
	    .pPoolSizes = &pool_size,
	};
	ret = vk->vkCreateDescriptorPool(vk->device, &dp_ci, NULL, &blend->desc_pool);
	if (ret != VK_SUCCESS) {
		return false;
	}

	// Render pass: single color attachment, LOAD/STORE, COLOR_ATTACHMENT in/out
	// (the atlas is already in COLOR_ATTACHMENT_OPTIMAL when content composites).
	VkAttachmentDescription att = {
	    .format = atlas_fmt,
	    .samples = VK_SAMPLE_COUNT_1_BIT,
	    .loadOp = VK_ATTACHMENT_LOAD_OP_LOAD,
	    .storeOp = VK_ATTACHMENT_STORE_OP_STORE,
	    .stencilLoadOp = VK_ATTACHMENT_LOAD_OP_DONT_CARE,
	    .stencilStoreOp = VK_ATTACHMENT_STORE_OP_DONT_CARE,
	    .initialLayout = VK_IMAGE_LAYOUT_COLOR_ATTACHMENT_OPTIMAL,
	    .finalLayout = VK_IMAGE_LAYOUT_COLOR_ATTACHMENT_OPTIMAL,
	};
	VkAttachmentReference ref = {0, VK_IMAGE_LAYOUT_COLOR_ATTACHMENT_OPTIMAL};
	VkSubpassDescription sub = {
	    .pipelineBindPoint = VK_PIPELINE_BIND_POINT_GRAPHICS,
	    .colorAttachmentCount = 1,
	    .pColorAttachments = &ref,
	};
	VkRenderPassCreateInfo rp_ci = {
	    .sType = VK_STRUCTURE_TYPE_RENDER_PASS_CREATE_INFO,
	    .attachmentCount = 1,
	    .pAttachments = &att,
	    .subpassCount = 1,
	    .pSubpasses = &sub,
	};
	ret = vk->vkCreateRenderPass(vk->device, &rp_ci, NULL, &blend->render_pass);
	if (ret != VK_SUCCESS) {
		return false;
	}

	// Graphics pipeline: fullscreen triangle, SrcAlpha/InvSrcAlpha blend (the
	// fragment shader's coverage drives source alpha), dynamic viewport/scissor.
	VkPipelineShaderStageCreateInfo stages[2] = {
	    {
	        .sType = VK_STRUCTURE_TYPE_PIPELINE_SHADER_STAGE_CREATE_INFO,
	        .stage = VK_SHADER_STAGE_VERTEX_BIT,
	        .module = blend->vert_mod,
	        .pName = "main",
	    },
	    {
	        .sType = VK_STRUCTURE_TYPE_PIPELINE_SHADER_STAGE_CREATE_INFO,
	        .stage = VK_SHADER_STAGE_FRAGMENT_BIT,
	        .module = blend->frag_mod,
	        .pName = "main",
	    },
	};

	VkPipelineVertexInputStateCreateInfo vi = {.sType =
	                                                VK_STRUCTURE_TYPE_PIPELINE_VERTEX_INPUT_STATE_CREATE_INFO};
	VkPipelineInputAssemblyStateCreateInfo ia = {
	    .sType = VK_STRUCTURE_TYPE_PIPELINE_INPUT_ASSEMBLY_STATE_CREATE_INFO,
	    .topology = VK_PRIMITIVE_TOPOLOGY_TRIANGLE_LIST};
	VkPipelineViewportStateCreateInfo vps = {.sType = VK_STRUCTURE_TYPE_PIPELINE_VIEWPORT_STATE_CREATE_INFO,
	                                         .viewportCount = 1,
	                                         .scissorCount = 1};
	VkPipelineRasterizationStateCreateInfo rs = {
	    .sType = VK_STRUCTURE_TYPE_PIPELINE_RASTERIZATION_STATE_CREATE_INFO,
	    .polygonMode = VK_POLYGON_MODE_FILL,
	    .cullMode = VK_CULL_MODE_NONE,
	    .frontFace = VK_FRONT_FACE_COUNTER_CLOCKWISE,
	    .lineWidth = 1.0f};
	VkPipelineMultisampleStateCreateInfo ms = {.sType =
	                                               VK_STRUCTURE_TYPE_PIPELINE_MULTISAMPLE_STATE_CREATE_INFO,
	                                           .rasterizationSamples = VK_SAMPLE_COUNT_1_BIT};
	VkPipelineColorBlendAttachmentState blend_att = {
	    .blendEnable = VK_TRUE,
	    // Leave the atlas alpha untouched (same rationale as vk_hud_blend) — the
	    // weave/present path may treat alpha as window transparency.
	    .colorWriteMask = VK_COLOR_COMPONENT_R_BIT | VK_COLOR_COMPONENT_G_BIT | VK_COLOR_COMPONENT_B_BIT,
	    .srcColorBlendFactor = VK_BLEND_FACTOR_SRC_ALPHA,
	    .dstColorBlendFactor = VK_BLEND_FACTOR_ONE_MINUS_SRC_ALPHA,
	    .colorBlendOp = VK_BLEND_OP_ADD,
	    .srcAlphaBlendFactor = VK_BLEND_FACTOR_ZERO,
	    .dstAlphaBlendFactor = VK_BLEND_FACTOR_ONE,
	    .alphaBlendOp = VK_BLEND_OP_ADD};
	VkPipelineColorBlendStateCreateInfo cb = {.sType =
	                                              VK_STRUCTURE_TYPE_PIPELINE_COLOR_BLEND_STATE_CREATE_INFO,
	                                          .attachmentCount = 1,
	                                          .pAttachments = &blend_att};
	VkPipelineDepthStencilStateCreateInfo ds = {
	    .sType = VK_STRUCTURE_TYPE_PIPELINE_DEPTH_STENCIL_STATE_CREATE_INFO};
	VkDynamicState dyn_states[] = {VK_DYNAMIC_STATE_VIEWPORT, VK_DYNAMIC_STATE_SCISSOR};
	VkPipelineDynamicStateCreateInfo dyn = {.sType =
	                                            VK_STRUCTURE_TYPE_PIPELINE_DYNAMIC_STATE_CREATE_INFO,
	                                        .dynamicStateCount = 2,
	                                        .pDynamicStates = dyn_states};

	VkGraphicsPipelineCreateInfo pipe_ci = {
	    .sType = VK_STRUCTURE_TYPE_GRAPHICS_PIPELINE_CREATE_INFO,
	    .stageCount = 2,
	    .pStages = stages,
	    .pVertexInputState = &vi,
	    .pInputAssemblyState = &ia,
	    .pViewportState = &vps,
	    .pRasterizationState = &rs,
	    .pMultisampleState = &ms,
	    .pDepthStencilState = &ds,
	    .pColorBlendState = &cb,
	    .pDynamicState = &dyn,
	    .layout = blend->pipe_layout,
	    .renderPass = blend->render_pass,
	    .subpass = 0,
	};
	ret = vk->vkCreateGraphicsPipelines(vk->device, VK_NULL_HANDLE, 1, &pipe_ci, NULL, &blend->pipeline);
	if (ret != VK_SUCCESS) {
		U_LOG_E("[content blend] Failed to create pipeline: %d", ret);
		return false;
	}

	blend->initialized = true;
	return true;
}

/*!
 * Cached 2D image view for a (image, array-layer) source in the image's native
 * format, created on first sight. Shared by content and HUD sources.
 */
static VkImageView
get_or_create_view(
    struct comp_multi_content_blend *blend, struct vk_bundle *vk, VkImage image, uint32_t array_layer, VkFormat fmt)
{
	for (uint32_t i = 0; i < blend->view_count; i++) {
		if (blend->cached_views[i].image == image && blend->cached_views[i].array_layer == array_layer) {
			return blend->cached_views[i].view;
		}
	}
	if (blend->view_count >= COMP_MULTI_CONTENT_BLEND_MAX_VIEWS) {
		U_LOG_E("[content blend] view cache exhausted (max=%u)", COMP_MULTI_CONTENT_BLEND_MAX_VIEWS);
		return VK_NULL_HANDLE;
	}
	VkImageViewCreateInfo view_ci = {
	    .sType = VK_STRUCTURE_TYPE_IMAGE_VIEW_CREATE_INFO,
	    .image = image,
	    .viewType = VK_IMAGE_VIEW_TYPE_2D,
	    .format = fmt,
	    .subresourceRange = {VK_IMAGE_ASPECT_COLOR_BIT, 0, 1, array_layer, 1},
	};
	VkImageView view = VK_NULL_HANDLE;
	if (vk->vkCreateImageView(vk->device, &view_ci, NULL, &view) != VK_SUCCESS) {
		U_LOG_E("[content blend] CreateImageView failed");
		return VK_NULL_HANDLE;
	}
	blend->cached_views[blend->view_count].image = image;
	blend->cached_views[blend->view_count].array_layer = array_layer;
	blend->cached_views[blend->view_count].view = view;
	blend->view_count++;
	return view;
}

/*!
 * Cached 2-binding descriptor set for a (content image+layer, HUD image) pair:
 * binding 0 samples the content view, binding 1 the HUD view. When the draw has
 * no HUD, @p hud_image equals @p content_image (self-bind) — a valid SHADER_READ
 * source that the shader never samples (hud_present = 0), so no dummy texture is
 * needed. The HUD source is always a 2D, layer-0 image.
 */
static VkDescriptorSet
get_or_create_image_desc(struct comp_multi_content_blend *blend,
                         struct vk_bundle *vk,
                         VkImage content_image,
                         uint32_t content_layer,
                         VkFormat content_fmt,
                         VkImage hud_image,
                         VkFormat hud_fmt)
{
	for (uint32_t i = 0; i < blend->desc_count; i++) {
		if (blend->cached_descs[i].content_image == content_image &&
		    blend->cached_descs[i].content_layer == content_layer &&
		    blend->cached_descs[i].hud_image == hud_image) {
			return blend->cached_descs[i].desc_set;
		}
	}
	if (blend->desc_count >= COMP_MULTI_CONTENT_BLEND_MAX_DESCS) {
		U_LOG_E("[content blend] desc cache exhausted (max=%u)", COMP_MULTI_CONTENT_BLEND_MAX_DESCS);
		return VK_NULL_HANDLE;
	}

	VkImageView content_view = get_or_create_view(blend, vk, content_image, content_layer, content_fmt);
	VkImageView hud_view = get_or_create_view(blend, vk, hud_image, 0, hud_fmt);
	if (content_view == VK_NULL_HANDLE || hud_view == VK_NULL_HANDLE) {
		return VK_NULL_HANDLE;
	}

	VkDescriptorSetAllocateInfo ds_ai = {
	    .sType = VK_STRUCTURE_TYPE_DESCRIPTOR_SET_ALLOCATE_INFO,
	    .descriptorPool = blend->desc_pool,
	    .descriptorSetCount = 1,
	    .pSetLayouts = &blend->desc_layout,
	};
	VkDescriptorSet desc_set = VK_NULL_HANDLE;
	if (vk->vkAllocateDescriptorSets(vk->device, &ds_ai, &desc_set) != VK_SUCCESS) {
		U_LOG_E("[content blend] AllocateDescriptorSets failed");
		return VK_NULL_HANDLE;
	}

	VkDescriptorImageInfo infos[2] = {
	    {
	        .sampler = blend->sampler,
	        .imageView = content_view,
	        .imageLayout = VK_IMAGE_LAYOUT_SHADER_READ_ONLY_OPTIMAL,
	    },
	    {
	        .sampler = blend->sampler,
	        .imageView = hud_view,
	        .imageLayout = VK_IMAGE_LAYOUT_SHADER_READ_ONLY_OPTIMAL,
	    },
	};
	VkWriteDescriptorSet writes[2] = {
	    {
	        .sType = VK_STRUCTURE_TYPE_WRITE_DESCRIPTOR_SET,
	        .dstSet = desc_set,
	        .dstBinding = 0,
	        .descriptorCount = 1,
	        .descriptorType = VK_DESCRIPTOR_TYPE_COMBINED_IMAGE_SAMPLER,
	        .pImageInfo = &infos[0],
	    },
	    {
	        .sType = VK_STRUCTURE_TYPE_WRITE_DESCRIPTOR_SET,
	        .dstSet = desc_set,
	        .dstBinding = 1,
	        .descriptorCount = 1,
	        .descriptorType = VK_DESCRIPTOR_TYPE_COMBINED_IMAGE_SAMPLER,
	        .pImageInfo = &infos[1],
	    },
	};
	vk->vkUpdateDescriptorSets(vk->device, 2, writes, 0, NULL);

	blend->cached_descs[blend->desc_count].content_image = content_image;
	blend->cached_descs[blend->desc_count].content_layer = content_layer;
	blend->cached_descs[blend->desc_count].hud_image = hud_image;
	blend->cached_descs[blend->desc_count].desc_set = desc_set;
	blend->desc_count++;
	return desc_set;
}

void
comp_multi_content_blend_begin(
    struct comp_multi_content_blend *blend, struct vk_bundle *vk, VkCommandBuffer cmd, VkFramebuffer fb, uint32_t fb_w, uint32_t fb_h)
{
	if (!blend->initialized || fb == VK_NULL_HANDLE) {
		return;
	}

	VkRenderPassBeginInfo rp_bi = {
	    .sType = VK_STRUCTURE_TYPE_RENDER_PASS_BEGIN_INFO,
	    .renderPass = blend->render_pass,
	    .framebuffer = fb,
	    .renderArea = {{0, 0}, {fb_w, fb_h}},
	};
	// Begin the pass only — each draw binds its own pipeline (axis-aligned or
	// quad) so the two interleave freely in one pass and preserve painter order.
	vk->vkCmdBeginRenderPass(cmd, &rp_bi, VK_SUBPASS_CONTENTS_INLINE);
}

void
comp_multi_content_blend_draw(struct comp_multi_content_blend *blend,
                              struct vk_bundle *vk,
                              VkCommandBuffer cmd,
                              VkImage src_image,
                              uint32_t array_layer,
                              VkFormat src_fmt,
                              VkImage hud_image,
                              VkFormat hud_fmt,
                              const struct comp_multi_content_pc *pc,
                              int32_t dst_x,
                              int32_t dst_y,
                              uint32_t dst_w,
                              uint32_t dst_h)
{
	if (!blend->initialized || src_image == VK_NULL_HANDLE || dst_w == 0 || dst_h == 0) {
		return;
	}

	// No HUD for this draw → self-bind the content image at binding 1 (a valid
	// SHADER_READ source the shader never samples since pc->hud_present == 0).
	if (hud_image == VK_NULL_HANDLE) {
		hud_image = src_image;
		hud_fmt = src_fmt;
	}
	VkDescriptorSet desc_set =
	    get_or_create_image_desc(blend, vk, src_image, array_layer, src_fmt, hud_image, hud_fmt);
	if (desc_set == VK_NULL_HANDLE) {
		return;
	}

	vk->vkCmdBindPipeline(cmd, VK_PIPELINE_BIND_POINT_GRAPHICS, blend->pipeline);
	VkViewport vp = {(float)dst_x, (float)dst_y, (float)dst_w, (float)dst_h, 0.0f, 1.0f};
	vk->vkCmdSetViewport(cmd, 0, 1, &vp);
	VkRect2D scissor = {{dst_x, dst_y}, {dst_w, dst_h}};
	vk->vkCmdSetScissor(cmd, 0, 1, &scissor);

	vk->vkCmdPushConstants(cmd, blend->pipe_layout, VK_SHADER_STAGE_FRAGMENT_BIT, 0,
	                       sizeof(struct comp_multi_content_pc), pc);
	vk->vkCmdBindDescriptorSets(cmd, VK_PIPELINE_BIND_POINT_GRAPHICS, blend->pipe_layout, 0, 1, &desc_set, 0,
	                            NULL);
	vk->vkCmdDraw(cmd, 3, 1, 0, 0);
}

/*!
 * Lazily build the rotated-window (quad) pipeline: a TRIANGLE_STRIP pipeline
 * with a VERTEX|FRAGMENT push-constant range, sharing the descriptor layout,
 * render pass and (implicitly) sampler/desc cache of the axis-aligned pipeline.
 */
static bool
ensure_quad_pipeline(struct comp_multi_content_blend *blend, struct vk_bundle *vk)
{
	if (blend->quad_initialized) {
		return true;
	}
	VkResult ret;

	VkShaderModuleCreateInfo vert_ci = {
	    .sType = VK_STRUCTURE_TYPE_SHADER_MODULE_CREATE_INFO,
	    .codeSize = sizeof(content_quad_vert_spv),
	    .pCode = content_quad_vert_spv,
	};
	ret = vk->vkCreateShaderModule(vk->device, &vert_ci, NULL, &blend->quad_vert_mod);
	if (ret != VK_SUCCESS) {
		U_LOG_E("[content blend] Failed to create quad vert shader: %d", ret);
		return false;
	}
	VkShaderModuleCreateInfo frag_ci = {
	    .sType = VK_STRUCTURE_TYPE_SHADER_MODULE_CREATE_INFO,
	    .codeSize = sizeof(content_quad_frag_spv),
	    .pCode = content_quad_frag_spv,
	};
	ret = vk->vkCreateShaderModule(vk->device, &frag_ci, NULL, &blend->quad_frag_mod);
	if (ret != VK_SUCCESS) {
		U_LOG_E("[content blend] Failed to create quad frag shader: %d", ret);
		return false;
	}

	// The vertex stage reads the projected corners; the fragment stage reads the
	// SDF params + sub-rect — so the range spans both stages.
	VkPushConstantRange pc_range = {
	    .stageFlags = VK_SHADER_STAGE_VERTEX_BIT | VK_SHADER_STAGE_FRAGMENT_BIT,
	    .offset = 0,
	    .size = sizeof(struct comp_multi_content_pc_quad),
	};
	VkPipelineLayoutCreateInfo pl_ci = {
	    .sType = VK_STRUCTURE_TYPE_PIPELINE_LAYOUT_CREATE_INFO,
	    .setLayoutCount = 1,
	    .pSetLayouts = &blend->desc_layout,
	    .pushConstantRangeCount = 1,
	    .pPushConstantRanges = &pc_range,
	};
	ret = vk->vkCreatePipelineLayout(vk->device, &pl_ci, NULL, &blend->quad_pipe_layout);
	if (ret != VK_SUCCESS) {
		return false;
	}

	VkPipelineShaderStageCreateInfo stages[2] = {
	    {
	        .sType = VK_STRUCTURE_TYPE_PIPELINE_SHADER_STAGE_CREATE_INFO,
	        .stage = VK_SHADER_STAGE_VERTEX_BIT,
	        .module = blend->quad_vert_mod,
	        .pName = "main",
	    },
	    {
	        .sType = VK_STRUCTURE_TYPE_PIPELINE_SHADER_STAGE_CREATE_INFO,
	        .stage = VK_SHADER_STAGE_FRAGMENT_BIT,
	        .module = blend->quad_frag_mod,
	        .pName = "main",
	    },
	};
	VkPipelineVertexInputStateCreateInfo vi = {.sType =
	                                                VK_STRUCTURE_TYPE_PIPELINE_VERTEX_INPUT_STATE_CREATE_INFO};
	VkPipelineInputAssemblyStateCreateInfo ia = {
	    .sType = VK_STRUCTURE_TYPE_PIPELINE_INPUT_ASSEMBLY_STATE_CREATE_INFO,
	    .topology = VK_PRIMITIVE_TOPOLOGY_TRIANGLE_STRIP};
	VkPipelineViewportStateCreateInfo vps = {.sType = VK_STRUCTURE_TYPE_PIPELINE_VIEWPORT_STATE_CREATE_INFO,
	                                         .viewportCount = 1,
	                                         .scissorCount = 1};
	VkPipelineRasterizationStateCreateInfo rs = {
	    .sType = VK_STRUCTURE_TYPE_PIPELINE_RASTERIZATION_STATE_CREATE_INFO,
	    .polygonMode = VK_POLYGON_MODE_FILL,
	    .cullMode = VK_CULL_MODE_NONE,
	    .frontFace = VK_FRONT_FACE_COUNTER_CLOCKWISE,
	    .lineWidth = 1.0f};
	VkPipelineMultisampleStateCreateInfo ms = {.sType =
	                                               VK_STRUCTURE_TYPE_PIPELINE_MULTISAMPLE_STATE_CREATE_INFO,
	                                           .rasterizationSamples = VK_SAMPLE_COUNT_1_BIT};
	VkPipelineColorBlendAttachmentState blend_att = {
	    .blendEnable = VK_TRUE,
	    .colorWriteMask = VK_COLOR_COMPONENT_R_BIT | VK_COLOR_COMPONENT_G_BIT | VK_COLOR_COMPONENT_B_BIT,
	    .srcColorBlendFactor = VK_BLEND_FACTOR_SRC_ALPHA,
	    .dstColorBlendFactor = VK_BLEND_FACTOR_ONE_MINUS_SRC_ALPHA,
	    .colorBlendOp = VK_BLEND_OP_ADD,
	    .srcAlphaBlendFactor = VK_BLEND_FACTOR_ZERO,
	    .dstAlphaBlendFactor = VK_BLEND_FACTOR_ONE,
	    .alphaBlendOp = VK_BLEND_OP_ADD};
	VkPipelineColorBlendStateCreateInfo cb = {.sType =
	                                              VK_STRUCTURE_TYPE_PIPELINE_COLOR_BLEND_STATE_CREATE_INFO,
	                                          .attachmentCount = 1,
	                                          .pAttachments = &blend_att};
	VkPipelineDepthStencilStateCreateInfo ds = {
	    .sType = VK_STRUCTURE_TYPE_PIPELINE_DEPTH_STENCIL_STATE_CREATE_INFO};
	VkDynamicState dyn_states[] = {VK_DYNAMIC_STATE_VIEWPORT, VK_DYNAMIC_STATE_SCISSOR};
	VkPipelineDynamicStateCreateInfo dyn = {.sType =
	                                            VK_STRUCTURE_TYPE_PIPELINE_DYNAMIC_STATE_CREATE_INFO,
	                                        .dynamicStateCount = 2,
	                                        .pDynamicStates = dyn_states};
	VkGraphicsPipelineCreateInfo pipe_ci = {
	    .sType = VK_STRUCTURE_TYPE_GRAPHICS_PIPELINE_CREATE_INFO,
	    .stageCount = 2,
	    .pStages = stages,
	    .pVertexInputState = &vi,
	    .pInputAssemblyState = &ia,
	    .pViewportState = &vps,
	    .pRasterizationState = &rs,
	    .pMultisampleState = &ms,
	    .pDepthStencilState = &ds,
	    .pColorBlendState = &cb,
	    .pDynamicState = &dyn,
	    .layout = blend->quad_pipe_layout,
	    .renderPass = blend->render_pass,
	    .subpass = 0,
	};
	ret = vk->vkCreateGraphicsPipelines(vk->device, VK_NULL_HANDLE, 1, &pipe_ci, NULL, &blend->quad_pipeline);
	if (ret != VK_SUCCESS) {
		U_LOG_E("[content blend] Failed to create quad pipeline: %d", ret);
		return false;
	}

	blend->quad_initialized = true;
	return true;
}

void
comp_multi_content_blend_draw_quad(struct comp_multi_content_blend *blend,
                                   struct vk_bundle *vk,
                                   VkCommandBuffer cmd,
                                   VkImage src_image,
                                   uint32_t array_layer,
                                   VkFormat src_fmt,
                                   VkImage hud_image,
                                   VkFormat hud_fmt,
                                   const struct comp_multi_content_pc_quad *pc,
                                   int32_t scissor_x,
                                   int32_t scissor_y,
                                   uint32_t scissor_w,
                                   uint32_t scissor_h,
                                   uint32_t atlas_w,
                                   uint32_t atlas_h)
{
	if (!blend->initialized || src_image == VK_NULL_HANDLE || scissor_w == 0 || scissor_h == 0) {
		return;
	}
	if (!ensure_quad_pipeline(blend, vk)) {
		return;
	}

	// No HUD for this draw → self-bind the content image at binding 1 (chrome
	// pills / overlays pass hud_present == 0, so binding 1 is never sampled).
	if (hud_image == VK_NULL_HANDLE) {
		hud_image = src_image;
		hud_fmt = src_fmt;
	}
	VkDescriptorSet desc_set =
	    get_or_create_image_desc(blend, vk, src_image, array_layer, src_fmt, hud_image, hud_fmt);
	if (desc_set == VK_NULL_HANDLE) {
		return;
	}

	vk->vkCmdBindPipeline(cmd, VK_PIPELINE_BIND_POINT_GRAPHICS, blend->quad_pipeline);
	// Viewport spans the whole atlas — the VS emits absolute NDC for the projected
	// corners, so the viewport just maps NDC→atlas px 1:1. The scissor confines the
	// tilted quad to its eye tile (it can otherwise spill into the other eye / off-atlas).
	VkViewport vp = {0.0f, 0.0f, (float)atlas_w, (float)atlas_h, 0.0f, 1.0f};
	vk->vkCmdSetViewport(cmd, 0, 1, &vp);
	VkRect2D scissor = {{scissor_x, scissor_y}, {scissor_w, scissor_h}};
	vk->vkCmdSetScissor(cmd, 0, 1, &scissor);

	vk->vkCmdPushConstants(cmd, blend->quad_pipe_layout,
	                       VK_SHADER_STAGE_VERTEX_BIT | VK_SHADER_STAGE_FRAGMENT_BIT, 0,
	                       sizeof(struct comp_multi_content_pc_quad), pc);
	vk->vkCmdBindDescriptorSets(cmd, VK_PIPELINE_BIND_POINT_GRAPHICS, blend->quad_pipe_layout, 0, 1, &desc_set, 0,
	                            NULL);
	vk->vkCmdDraw(cmd, 4, 1, 0, 0);
}

void
comp_multi_content_blend_end(struct comp_multi_content_blend *blend, struct vk_bundle *vk, VkCommandBuffer cmd)
{
	if (!blend->initialized) {
		return;
	}
	vk->vkCmdEndRenderPass(cmd);
}

void
comp_multi_content_blend_fini(struct comp_multi_content_blend *blend, struct vk_bundle *vk)
{
	if (!blend->initialized) {
		return;
	}

	for (uint32_t i = 0; i < blend->view_count; i++) {
		vk->vkDestroyImageView(vk->device, blend->cached_views[i].view, NULL);
		// Descriptor sets freed implicitly with the pool.
	}

	if (blend->quad_pipeline != VK_NULL_HANDLE) {
		vk->vkDestroyPipeline(vk->device, blend->quad_pipeline, NULL);
	}
	if (blend->quad_pipe_layout != VK_NULL_HANDLE) {
		vk->vkDestroyPipelineLayout(vk->device, blend->quad_pipe_layout, NULL);
	}
	if (blend->quad_vert_mod != VK_NULL_HANDLE) {
		vk->vkDestroyShaderModule(vk->device, blend->quad_vert_mod, NULL);
	}
	if (blend->quad_frag_mod != VK_NULL_HANDLE) {
		vk->vkDestroyShaderModule(vk->device, blend->quad_frag_mod, NULL);
	}
	if (blend->pipeline != VK_NULL_HANDLE) {
		vk->vkDestroyPipeline(vk->device, blend->pipeline, NULL);
	}
	if (blend->render_pass != VK_NULL_HANDLE) {
		vk->vkDestroyRenderPass(vk->device, blend->render_pass, NULL);
	}
	if (blend->pipe_layout != VK_NULL_HANDLE) {
		vk->vkDestroyPipelineLayout(vk->device, blend->pipe_layout, NULL);
	}
	if (blend->desc_pool != VK_NULL_HANDLE) {
		vk->vkDestroyDescriptorPool(vk->device, blend->desc_pool, NULL);
	}
	if (blend->desc_layout != VK_NULL_HANDLE) {
		vk->vkDestroyDescriptorSetLayout(vk->device, blend->desc_layout, NULL);
	}
	if (blend->sampler != VK_NULL_HANDLE) {
		vk->vkDestroySampler(vk->device, blend->sampler, NULL);
	}
	if (blend->vert_mod != VK_NULL_HANDLE) {
		vk->vkDestroyShaderModule(vk->device, blend->vert_mod, NULL);
	}
	if (blend->frag_mod != VK_NULL_HANDLE) {
		vk->vkDestroyShaderModule(vk->device, blend->frag_mod, NULL);
	}

	memset(blend, 0, sizeof(*blend));
}

